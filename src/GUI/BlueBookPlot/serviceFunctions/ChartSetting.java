package GUI.BlueBookPlot.serviceFunctions;

public class ChartSetting {

	public int x=0;
	public int y=0;
	
	public ChartSetting() {
		
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	
}
